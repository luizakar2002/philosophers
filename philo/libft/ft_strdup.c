#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*ptr;
	int		i;

	if (!(ptr = (char *)malloc(sizeof(char) * (ft_strlen(str) + 1))))
	{
		return (NULL);
	}
	i = 0;
	while (str[i])
	{
		ptr[i] = str[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
