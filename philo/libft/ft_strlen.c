#include "libft.h"

size_t		ft_strlen(const char *str)
{
	const char		*s;
	int				i;

	s = str;
	i = 0;
	if (str == NULL)
		return (0);
	while (*s != '\0')
	{
		i++;
		s++;
	}
	return (i);
}
